def sub_2_numbers(num1, num2):
    sum = num1 - num2
    return sum

numsum1 = sub_2_numbers(10, 20)
print( "Sum of {0} - {1} = {2}".format(10, 20, numsum1))

numsum1 = sub_2_numbers( 100, 1)
print( "Sum of {0} - {1} = {2}".format(100, 1, numsum1))

numsum1 = sub_2_numbers( 88, 20)
print( "Sum of {0} - {1} = {2}".format(88, 20, numsum1))
